def Sum(num1,num2):
    return num1 + num2
def Sub(num1,num2):
    return num1 - num2
def Mul(num1,num2):
    return num1 * num2
def Div(num1,num2):
    return num1 / num2
def Avg(num1,num2):
    return (num1 + num2)/2


u = Sum(13, 4)
v = Sub(13, 4)
w = Mul(13, 4)
x = Div(13, 4)
y = Avg(13, 4)
print(u,v,w,x,y)
