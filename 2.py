num1 = input("Enter First Number: ")
num2 = input("Enter First Number: ")

float(num1)
float(num2)

if (num2>num1):
    print(f"{num2} is Greater than {num1}")
elif (num1>num2):
    print(f"{num1} is Greater than {num2}")
else:
    print(f"{num1} & {num2} are Equal Numbers")
